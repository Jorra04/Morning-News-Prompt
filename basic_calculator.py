class basic_calculator:
    def __init__(self):
        pass
    def add(self,x, y):
        return int(x) + int(y)
    def sub(self,x,y):
        return int(x) - int(y)
    def divide(self,x,y):
        return float(x) / float(y)
    def multiply(self,x,y):
        return int(x) * int(y)
